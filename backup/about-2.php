<h2 class="about-title">SILVERSMITH / LAMPWORK GLASS ARTIST / WEB DEVELOPER</h2>
<div class="padded-content">
<!-- <div class="about-me-left-side"> -->
<p class="first-paragraph">Art has always played an important role in my life. I played the piano, violin and harp as a child and spent most of my spare time with my nose in a book. As a teenager, I loved taking photographs with my SLR and developing them in the darkroom.</p>

<p class="first-paragraph">I studied politics then social health at university, with a particular interest in human rights.</p>

<h3 class="about-me-subheading">LAMPWORKING & SILVERSMITHING</h3>
<div class="box">
<p>In 2006, a friend introduced me to lampworking. I knew nothing about jewellery at the time but I was fascinated by hot glass and hooked from the moment I melted my first rod.</p>

<p>Later that week, I bought my first little torch, a hothead, and a few basic tools and rods of glass and set up a small studio at home. That small studio grew as I acquired more glass and tools and moved on to more powerful torches.</p>

<p>I find hot glass mesmerising and love playing with it and trying to get colours to react in different ways. <img src="/wp-content/uploads/2015/07/IMG_1198.jpg" alt="Lampwork glass bead being formed in the flame" width="180px" height="180px" class="text-wrap-left" />The array of reactions you can get from a single rod of silver-rich glass is astonishing and depends on so many variables - the temperature of the flame, the chemical makeup of the flame, the kiln environment, the types of tools used to manipulate the glass (brass vs graphite etc)...</p>

<p>Like so many artists/craftspeople, I first began selling my work as a way to fund my hobby but after a while my business became quite successful.
Unfortunately, in late 2010, I had to give up lampworking due to ill health. Since then, I've been back to university to study computer science.</p>

<p>From the time I started lampworking, I had wanted to try silversmithing and I was finally able to do beginners and intermediate courses at the incredible <a href="http://www.jamfactory.com.au/" target="_blank">Jam Factory</a> in Adelaide. </p>
</div><!-- box -->

<h3 class="about-me-subheading">THE IMPORTANCE OF HANDMADE</h3>
<div class="box">
<p>I'm a big proponent of buying handmade whenever possible - not just for the sake of the creator but for ethical and environmental reasons, too. I find that people who make things by hand are often interested in the origins of their materials and concerned about the impact their work might have on others and the world. Some materials cannot be obtained as ethically as I would like and that's a conflict I often find myself thinking about as I'm working.
I use recycled/reclaimed materials whenever possible and I always try to make my jewellery from 100% recycled sterling silver.</p>

<p>I also think handmade objects are inherently more interesting than mass-produced. They're never perfect but their imperfections make them unique and remind the owner that a real person put time and knowledge into creating their piece. I like knowing that the things I own are one of a kind.</p>

<p>My love for art and craft is fairly selfish - I get a great sense of satisfaction from the process of turning a pile of raw materials into something (hopefully) beautiful and/or useful.</p>
</div><!-- box -->

